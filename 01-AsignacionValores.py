#Existen 3 tipos de datos

#1.- NUMÉRICOS
numero = 10
print(numero)
print(type(numero))    #la función TYPE sirve para conocer el tipo de un objeto

numero2 = 10.56
print(numero2)
print(type(numero2))

#2.- TEXTOS
cadena = 'Hola'
print(cadena)
print(type(cadena))

cadena2 = "Estoy 'estudiando'"
print(cadena2)
print(type(cadena2))

#3.- BOOLEANOS
valor = True
print(valor)
print(type(valor))

valor2 = False
print(valor2)
print(type(valor2))

num1 = 10
num2 = 6.7
resultado = (num1+num2) * 10 / 6
print(resultado)
print("El resultado es: ",resultado)

